

ROOT="$HOME/rvsh"


function finger  {
    
	mailList=$(cat $ROOT/users/$1/mails)
	phoneList=$(cat $ROOT/users/$1/phones)

	echo "-------------------------------------------------------------------------------"
	echo -e "\t\t\t\tInfo about $1"
	echo "-------------------------------------Mails-------------------------------------"
	echo $mailList
	echo "-------------------------------------Phones------------------------------------"
	echo $phoneList
	echo "-------------------------------------------------------------------------------"


}